<?php

// for loop


for($i = 1; $i <= 10; $i++){
    echo $i . " hey, how are you!<br>";
}


// while loop

$j = 1;
while($j <= 10){
    echo $j. "<br>";
    $j++;
}

// echo phpversion();

// DO WHILE LOOP

echo "this is do while loop <br>";
$i = 1;

do{
    echo $i. "<br>";
    $i++;
}while($i <= 10);

?>

